    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (!txtUsername.getInputVerifier().verify(txtUsername)) {
            txtUsername.requestFocus();
            JOptionPane.showMessageDialog(rootPane,
                    "Username is not valid.\n"
                    + "You may only use letters numbers and underscore.",
                    "Username is not valid",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!txtPassword.getInputVerifier().verify(txtPassword)) {
            txtPassword.requestFocus();
            JOptionPane.showMessageDialog(rootPane,
                    "Password is eighter too short or too long!.\n"
                    + "Any character is acceptable and encouraged to use complex ones.",
                    "Password is not valid",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        canceled = false;
        this.dispose();
    }//GEN-LAST:event_btnSaveActionPerformed


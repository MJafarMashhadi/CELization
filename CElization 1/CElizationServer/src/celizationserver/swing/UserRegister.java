    public UserInfo getUserInfo(CElizationServer server) {
        UserInfo info;
        info = new UserInfo(txtUsername.getText(), txtPassword.getText());

        CElization game = server.getGame(lstGames.getSelectedItem().toString()).getGame();
        GameState newGameState = new GameState(game, txtUsername.getText());

        info.setGameInstance(newGameState);

        return info;
    }

    public Integer getPort(CElizationServer server) {
        return server.getPort(lstGames.getSelectedItem().toString());
    }

    public NaturalResources getInitialNaturalResources() {
        return new NaturalResources(
                Integer.valueOf(spnGold.getValue().toString()),
                Integer.valueOf(spnStone.getValue().toString()),
                Integer.valueOf(spnLumber.getValue().toString()), Integer.valueOf(spnFood.getValue().toString()), 0);
    }
